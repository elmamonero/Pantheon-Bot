async function tagemoji(m, { conn }) {
  // Verifica que el comando solo se use en grupos
  if (!m.isGroup) throw 'Este comando solo puede usarse en grupos.';

  // Obt√©n todos los miembros del grupo
  const groupMeta = await conn.groupMetadata(m.chat);
  const participants = groupMeta.participants.map(p => p.id);

  // Construye el texto con menciones y emojis aleatorios
  let text = '';
  for (const user of participants) {
    text += `${randomEmoji()} @${user.split('@')[0]}\n`;
  }

  // Env√≠a el mensaje con menciones a todos
  await conn.sendMessage(m.chat, { text, mentions: participants }, { quoted: m });
}

